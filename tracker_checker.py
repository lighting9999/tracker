def check_http_tracker(url: str, timeout: int) -> Tuple[str, Optional[int]]:
    session = requests.Session()
    retries = Retry(total=0, connect=0, read=0, redirect=0)
    adapter = HTTPAdapter(max_retries=retries)
    session.mount('http://', adapter)
    session.mount('https://', adapter)

    peer_id_random = ''.join(str(random.randint(0, 9)) for _ in range(12))
    peer_id = f"-RS0001-{peer_id_random}"
    params = {
        'info_hash': '00000000000000000000',
        'peer_id': peer_id,
        'port': '6881',
        'uploaded': '0',
        'downloaded': '0',
        'left': '0',
        'compact': '1',
        'event': 'started'
    }

    try:
        start = time.time()
        resp = session.get(url, params=params, timeout=timeout, allow_redirects=False)
        elapsed = int((time.time() - start) * 1000)
    except Exception:
        return Status.DEAD, None

    body = resp.content

    if is_parked_or_expired_domain(body):
        return Status.INVALID, elapsed

    if bdecode_simple(body):
        return Status.ALIVE, elapsed

    if resp.status_code == 200:
        head = body[:200].decode('utf-8', errors='replace').lower()
        if any(tag in head for tag in ('<html', '<body', '<head')):
            return Status.INVALID, elapsed
        if len(body) > 50000:
            return Status.INVALID, elapsed
        return Status.DEAD, elapsed

    if resp.status_code in (400, 403) and bdecode_simple(body):
        return Status.ALIVE, elapsed

    return Status.DEAD, elapsed

def check_udp_tracker(url: str, timeout: int) -> Tuple[str, Optional[int]]:
    try:
        parsed = urlparse(url)
        host = parsed.hostname
        port = parsed.port or 80
    except ValueError:
        return Status.INVALID, None

    if not host:
        return Status.INVALID, None

    try:
        sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        sock.settimeout(timeout)
    except OSError:
        return Status.DEAD, None

    conn_id = 0x41727101980
    action = 0
    trans_id = random.randint(0, 0xFFFFFFFF)
    packet = conn_id.to_bytes(8, 'big') + action.to_bytes(4, 'big') + trans_id.to_bytes(4, 'big')

    try:
        start = time.time()
        sock.sendto(packet, (host, port))
        data, _ = sock.recvfrom(16)
        elapsed = int((time.time() - start) * 1000)
        return Status.ALIVE, elapsed
    except OSError:
        return Status.DEAD, None
    finally:
        sock.close()

def check_wss_tracker(url: str, timeout: int) -> Tuple[str, Optional[int]]:
    try:
        parsed = urlparse(url)
        host = parsed.hostname
        port = parsed.port or 443
    except ValueError:
        return Status.INVALID, None

    if not host:
        return Status.INVALID, None

    try:
        start = time.time()
        sock = socket.create_connection((host, port), timeout=timeout)
        sock.close()
        elapsed = int((time.time() - start) * 1000)
        return Status.ALIVE, elapsed
    except OSError:
        return Status.DEAD, None

def validate_tracker(url: str, timeout: int) -> CheckResult:
    try:
        parsed = urlparse(url)
    except ValueError:
        return CheckResult(url, Status.INVALID)

    scheme = parsed.scheme.lower()
    if scheme in ('http', 'https'):
        status, ping = check_http_tracker(url, timeout)
    elif scheme == 'udp':
        status, ping = check_udp_tracker(url, timeout)
    elif scheme == 'wss':
        status, ping = check_wss_tracker(url, timeout)
    else:
        status, ping = Status.INVALID, None

    return CheckResult(url, status, ping)

