import argparse
import os
import re
import random
import socket
import sys
import time
from collections import defaultdict
from concurrent.futures import ThreadPoolExecutor, as_completed
from dataclasses import dataclass
from pathlib import Path
from typing import List, Optional, Dict, Tuple
from urllib.parse import urlparse, ParseResult, urlunparse

import requests
from requests.adapters import HTTPAdapter
from urllib3.util.retry import Retry

# ----------------------------------------------------------------------
# 常量
DEFAULT_TIMEOUT = 10          # 秒
DEFAULT_WORKERS = 20
OUTPUT_FILES = [
    "trackers_all.txt",
    "trackers_best.txt",
    "trackers_best_http.txt",
    "trackers_best_https.txt",
    "trackers_best_udp.txt",
    "trackers_best_wss.txt",
]

# ----------------------------------------------------------------------
class Status:
    ALIVE = "ALIVE"
    DEAD = "DEAD"
    INVALID = "INVALID"

# ----------------------------------------------------------------------
@dataclass
class CheckResult:
    url: str
    status: str
    ping_ms: Optional[int] = None

@dataclass
class TrackerHistory:
    checks: int = 0
    alive_checks: int = 0
    first_seen_ts: int = 0
    first_alive_ts: int = 0
    last_seen_ts: int = 0
    last_alive_ts: int = 0
    streak_alive_start_ts: int = 0

# ----------------------------------------------------------------------
def parse_tracker_line(line: str) -> str:
    line = line.strip()
    if line.startswith('[') and '](' in line and line.endswith(')'):
        _, _, right = line.partition('](')
        return right[:-1]
    return line

TRACKER_REGEX = re.compile(r'(?i)(https?|udp|wss)://[^\s,]*?/announce')

def collapse_path_slashes(path: str) -> str:
    result = []
    prev = False
    for ch in path:
        if ch == '/':
            if not prev:
                result.append(ch)
            prev = True
        else:
            result.append(ch)
            prev = False
    out = ''.join(result)
    return '/' if out == '' else out

def normalize_tracker_url(raw: str) -> Optional[str]:
    candidate = raw.strip().strip('\'"<>[](){};,.')

    try:
        parsed = urlparse(candidate)
    except ValueError:
        return None

    scheme = parsed.scheme.lower()
    if scheme not in ('http', 'https', 'udp', 'wss'):
        return None

    host = parsed.hostname
    if not host:
        return None

    try:
        socket.inet_aton(host)
        is_ip = True
    except OSError:
        is_ip = False
    if not is_ip and '.' not in host:
        return None

    path = collapse_path_slashes(parsed.path)
    if not path.endswith('/announce'):
        return None

    new_parsed = ParseResult(
        scheme=scheme,
        netloc=parsed.netloc,
        path=path,
        params='',
        query='',
        fragment=''
    )

    if (scheme == 'http' and parsed.port == 80) or (scheme == 'https' and parsed.port == 443):
        new_parsed = new_parsed._replace(netloc=host)

    return urlunparse(new_parsed)

def bdecode_simple(data: bytes) -> bool:
    if not data or data[0] != ord('d'):
        return False
    return any(kw in data for kw in (b'interval', b'peers', b'failure reason'))

def is_parked_or_expired_domain(content: bytes) -> bool:
    text = content.decode('utf-8', errors='replace').lower()
    indicators = [
        "domain for sale", "domain is for sale", "buy this domain",
        "domain expired", "domain has expired", "this domain is expired",
        "register this domain", "domain available", "parked domain",
        "domain parking", "coming soon", "under construction",
        "page not found", "404 not found", "namecheap",
        "godaddy parked", "sedo domain parking", "domain portfolio",
        "premium domain", "afternic", "escrow.com", "dan.com/buy-domain",
        "undeveloped", "this webpage was generated by the domain owner",
        "hugedomains.com", "bodis.com", "sedoparking"
    ]
    if any(ind in text for ind in indicators):
        return True
    head = content[:500].decode('utf-8', errors='replace').lower()
    return '<html' in head or '<!doctype html' in head

